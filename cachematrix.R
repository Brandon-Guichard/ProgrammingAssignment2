## This function calculates the inverse matrix for any provided matrix with the same number of rows / columns (i.e. 2X2, 3X3, etc)

## The makeMatrix function creates an object that stores the original matrix value and the inverse if it has already been calculated

makeMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setsolve <- function(solve) m <<- solve
        getsolve <- function() m
        list(set = set, get = get,
             setsolve = setsolve,
             getsolve = getsolve)
}


## This function calculates the inverse matrix and stores that value

cacheMatrix <- function(x, ...) {
        m <- x$getsolve()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setsolve(m)
        m
}